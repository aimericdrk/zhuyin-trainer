// Stroke-count quiz generator: show a character, pick its number of strokes.
// Emits a self-contained quiz item (own numeric options) for useGameQuiz.
function shuffleWith(arr, rand) {
  const a = arr.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function generateStrokeCount(rand = Math.random, prevId = null) {
  let candidates = WRITING_CHARS;
  if (prevId) candidates = WRITING_CHARS.filter((c) => `stk-${c.zh}` !== prevId);
  const target = shuffleWith(candidates, rand)[0];
  const counts = new Set([target.strokes]);
  let delta = 1;
  while (counts.size < 6) {
    if (target.strokes - delta >= 1) counts.add(target.strokes - delta);
    if (counts.size < 6) counts.add(target.strokes + delta);
    delta += 1;
  }
  const options = shuffleWith([...counts], rand).map((n) => ({ id: `n${n}`, zh: String(n), py: '' }));
  return {
    id: `stk-${target.zh}`,
    prompt: { zh: target.zh, gloss: { en: 'How many strokes?', fr: 'Combien de traits ?' } },
    options,
    correctId: `n${target.strokes}`,
    audio: target.zh,
    autoPlay: false,
  };
}
